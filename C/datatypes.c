#include <stdio.h>

int main() {

    // Characters
    int number = '3';
    char letter = 'a';
    char bracket = ']';

    printf("Number: %c, Letter: %c, Bracket: %c\n", number, letter, bracket);

    // Numbers
    int value = 6;
    float floatingNumber = 50.0;

    printf("Value: %d, Floating Number: %.2f\n", value, floatingNumber);


}
